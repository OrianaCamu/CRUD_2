    void listar(){
        String sql= "SELECT * FROM alumnos";
        try {
            cn= con.getConnection();
            st=cn.createStatement();
            rs=st.executeQuery(sql);
            Object[]alumnos =new Object [4];
            modelo=(DefaultTableModel)tabladatos.getModel ();
            while (rs.next()){
               
             
                 
                alumnos [0]=rs.getInt ("id");
                alumnos [1]=rs.getString("nombre");
                alumnos [2]=rs.getString ("apellido");
                alumnos [3]=rs.getString ("DNI");
                modelo.addRow (alumnos);
                
            }
            tabladatos.setModel(modelo);
        } catch (Exception e) {
        }
    }
    
    void Agregar (){
        String nombre=txtnombre.getText();
        String apellido=txtapellido.getText();
        String DNI=txtDNI.getText();
        if (nombre.equals ("")||apellido.equals("")||DNI.equals ("")){
            JOptionPane.showMessageDialog (null, "campos vacios...!!!");
            
        }else{
            String sql="INSERT INTO alumnos (nombre, apellido,DNI) VALUES ('"+nombre+"','"+apellido+"', '"+DNI+"')";
            try {
                cn=con.getConnection();
                st=cn.createStatement ();
                st.executeUpdate (sql);
                JOptionPane.showMessageDialog(null, "Usuario Agregado...!!!");
                limpiartabla();
            } catch (Exception e) {
            }
        }
        
    }
    void limpiartabla (){
        for (int i=0;i<=tabladatos.getRowCount();i++){
            modelo.removeRow(i);
            i=i-1;
        }
    } 
